package com.asj.zj.bo.sys;

public class SysRole {
	private  int role_id;
	private  String role_name;
	private  String role_note;
	private  int region_code;
	private  int county_code;
	private  int org_id;
	public int getRole_id() {
		return role_id;
	}
	public void setRole_id(int role_id) {
		this.role_id = role_id;
	}
	public String getRole_name() {
		return role_name;
	}
	public void setRole_name(String role_name) {
		this.role_name = role_name;
	}
	public String getRole_note() {
		return role_note;
	}
	public void setRole_note(String role_note) {
		this.role_note = role_note;
	}
	public int getRegion_code() {
		return region_code;
	}
	public void setRegion_code(int region_code) {
		this.region_code = region_code;
	}
	public int getCounty_code() {
		return county_code;
	}
	public void setCounty_code(int county_code) {
		this.county_code = county_code;
	}
	public int getOrg_id() {
		return org_id;
	}
	public void setOrg_id(int org_id) {
		this.org_id = org_id;
	}

}
